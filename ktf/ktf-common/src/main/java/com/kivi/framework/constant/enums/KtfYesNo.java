package com.kivi.framework.constant.enums;

/**
 * YES or NO 枚举
 *
 */
public enum KtfYesNo {
    NO( 0, false, "0" ),
    YES( 1, true, "1" );

    public int     code;
    public boolean bool;
    public String  text;

    private KtfYesNo( int code, boolean bool, String text ) {
        this.code = code;
        this.bool = bool;
        this.text = text;
    }
}
